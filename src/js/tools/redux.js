// https://redux.js.org/recipes/reducing-boilerplate

export
function make_action_creator(type, ...arg_names) {
    return function(...args) {
        const action = { type };
        arg_names.forEach((arg, index) => {
            action[arg_names[index]] = args[index];
        });
        return action;
    };
}
